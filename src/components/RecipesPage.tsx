import React, { useState } from 'react';

const RecipesPage: React.FC = () => {
  const [recipe, setRecipe] = useState<string>('');

  // Массив с рецептами для похудения
  const recipes = [
    "Салат из свежих овощей: Нарежьте помидоры, огурцы, перец и лук. Заправьте оливковым маслом и лимонным соком.",
    "Куриная грудка на пару: Приготовьте куриную грудку на пару с добавлением специй. Подавайте с зеленью.",
    "Овсянка с фруктами: Сварите овсянку на воде. Добавьте нарезанные яблоки, бананы и немного меда.",
    "Тушеные овощи: Нарежьте кабачки, баклажаны, морковь и лук. Тушите на медленном огне с добавлением томатного соуса.",
    "Греческий йогурт с ягодами: Смешайте греческий йогурт с свежими или замороженными ягодами. Добавьте немного меда.",
    "Рыба на гриле: Приготовьте филе рыбы на гриле с лимоном и специями. Подавайте с овощным гарниром.",
    "Смузи из шпината и яблока: Смешайте шпинат, яблоко, банан и воду в блендере до однородной массы.",
    "Киноа с овощами: Отварите киноа и смешайте с нарезанными огурцами, помидорами и зеленью.",
    "Творог с зеленью: Смешайте обезжиренный творог с укропом, петрушкой и чесноком.",
    "Запеченные яблоки: Запеките яблоки в духовке с корицей и медом.",
  ];

  const fetchRecipe = () => {
    const randomIndex = Math.floor(Math.random() * recipes.length);
    setRecipe(recipes[randomIndex]);
  };

  return (
    <div className="recipes-page">
      <h2>Рецепты для похудения</h2>
      <button onClick={fetchRecipe}>Получить рецепт</button>
      {recipe && <p>{recipe}</p>}
    </div>
  );
};

export default RecipesPage;
import React, { useState } from 'react';

const MassGainRecipes: React.FC = () => {
  const [recipe, setRecipe] = useState<string>('');

  // Массив с рецептами для набора массы
  const massGainRecipes = [
    "Омлет с авокадо и сыром: Взбейте 3 яйца, добавьте нарезанный авокадо и тертый сыр. Жарьте на сковороде.",
    "Паста с курицей и сливочным соусом: Отварите пасту, обжарьте куриную грудку и смешайте со сливочным соусом.",
    "Протеиновый коктейль: Смешайте молоко, банан, овсянку, арахисовую пасту и протеиновый порошок в блендере.",
    "Рис с говядиной и овощами: Обжарьте говядину с овощами и подавайте с отварным рисом.",
    "Творог с медом и орехами: Смешайте творог с медом и добавьте измельченные орехи.",
    "Картофель с лососем: Запеките картофель и подавайте с запеченным лососем.",
    "Гречка с курицей и грибами: Обжарьте курицу с грибами и подавайте с гречкой.",
    "Сэндвич с индейкой и сыром: Соберите сэндвич из цельнозернового хлеба, индейки, сыра и овощей.",
    "Овсянка с молоком и сухофруктами: Сварите овсянку на молоке и добавьте сухофрукты.",
    "Запеканка из творога и яиц: Смешайте творог, яйца и изюм, запеките в духовке.",
  ];

  const fetchRecipe = () => {
    const randomIndex = Math.floor(Math.random() * massGainRecipes.length);
    setRecipe(massGainRecipes[randomIndex]);
  };

  return (
    <div className="recipes-page">
      <h2>Рецепты для набора массы</h2>
      <button onClick={fetchRecipe}>Получить рецепт</button>
      {recipe && <p>{recipe}</p>}
    </div>
  );
};

export default MassGainRecipes;